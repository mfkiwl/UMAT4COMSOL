      SUBROUTINE UMAT(STRESS,STATEV,DDSDDE,SSE,SPD,SCD,
     1           RPL,DDSDDT,DRPLDE,DRPLDT,
     2           STRAN,DSTRAN,TIME,DTIME,TEMP,DTEMP,PREDEF,
     2           DPRED,CMNAME,
     3           NDI,NSHR,NTENS,NSTATV,PROPS,NPROPS,COORDS,
     3           DROT,PNEWDT,
     4           CELENT,DFGRD0,DFGRD1,NOEL,NPT,
     5           LAYER,KSPT,KSTEP,KINC)

       INCLUDE 'ABA_PARAM.INC' 

      DIMENSiON STRESS(NTENS),STATEV(NSTATV),
     1     DDSDDE(NTENS,NTENS),DDSDDT(NTENS),DRPLDE(NTENS),
     2     STRAN(NTENS),DSTRAN(NTENS),TIME(2),PREDEF(1),DPRED(1),
     3     PROPS(NPROPS),DFGRD0(3,3),
     4     DFGRD1(3,3)
      
      real*8 stress3(6),dstran3_e(6),stress3_t(6)
      real*8 nflow3(6),eps_e(6),eps_e_t(6)
      real*8 ddsddeinv(3,3),ddsdde3(6,6),ddsdde3inv(6,6)
      real*8 newshear,newbulk,newhard,c33,e33t
      real*8 s_vm,s_hyd,syh,pseq_t,dpseq
      real*8 shear,bulk,EE,nu,sy,tol,tol2,rhs,R2
      integer ii,jj,itern,itern_max,iterps,iterps_max

c     MATERIAL PARAMETERS
      tol=1.0d-6
      tol2=1.0d-3
      itern_max=200
      iterps_max=20
      EE=props(1)
      nu=props(2)
      sy=props(3)
      hh=props(4)
      if(TIME(1).eq.0)then
      statev(1:7)=0.0d0
      endif

c     LOAD STATE
      eps_e_t(1:6)=statev(1:6)
      pseq_t=statev(7)
c     Stress3d
      if(ndi.eq.2)then
      stress3_t(1:2)=stress(1:2)
      stress3_t(4)=stress(3)
      stress3_t(3)=0.0d0
      stress3_t(5:6)=0.0d0
      else
      stress3_t(1:6)=stress(1:6)
      endif
      stress3(1:6)=stress3_t(1:6)
      dpseq=0.0d0

c     Loop for plane stress
      iterps=0
      R2=1.0d10
      itern=0
      do while((R2>tol2).and.(iterps<iterps_max))
      iterps=iterps+1

c     elastic strain accounting plane stress
      if(ndi.eq.2)then
      eps_e(1:2)=eps_e_t(1:2)+DSTRAN(1:2)
      eps_e(4)=eps_e_t(4)+DSTRAN(3)
      eps_e(5:6)=eps_e_t(5:6)
      if(iterps.eq.1)then
        e33t=eps_e_t(3)-nu/(1-nu)*(DSTRAN(1)+DSTRAN(2))
      else
        e33t=e33t-stress3(3)/c33
      endif
      eps_e(3)=e33t
      else
      eps_e(1:6)=eps_e_t(1:6)+DSTRAN(1:6)
      endif

c     Ddsdde3d
      shear=EE/(1.0d0+nu)/2.0d0
      bulk=(EE/(1.0d0-2.0d0*nu)-2.0d0*shear)/3.0d0
      ddsdde3(1:6,1:6)=0.0d0
      do ii=1,3
       do jj=1,3
        ddsdde3(ii,jj)=bulk
       enddo
       ddsdde3(ii,ii)=2.0d0*shear+bulk
      enddo
      do ii=4,6
       ddsdde3(ii,ii)=shear
      enddo
      c33=2.0d0*shear+bulk


c     Stress
      dstran3_e(1:6)=eps_e(1:6)-eps_e_t(1:6)
      stress3(1:6)=stress3_t(1:6)
      do ii=1,6
        do jj=1,6
          stress3(ii)=stress3(ii)+ddsdde3(ii,jj)*dstran3_e(jj)
        enddo
      enddo
     
c     Von mises
      s_vm=(stress3(1)-stress3(2))**2+(stress3(2)-stress3(3))**2
     1 +(stress3(3)-stress3(1))**2
      do ii=4,6
        s_vm=s_vm+6.d0*stress3(ii)**2
      enddo
      s_vm=sqrt(s_vm/2.0d0)



c     Hardening previous
      syh=sy+hh*pseq_t

c     yield
      dpseq=0.0d0
      itern=0
      if(s_vm.gt.(1.0d0+2.0d0*tol)*syh)then

c     Flow
      s_hyd=(stress3(1)+stress3(2)+stress3(3))/3.0d0
      nflow3(1:3)=(stress3(1:3)-s_hyd)/s_vm
      nflow3(4:6)=stress3(4:6)/s_vm
       
c     Newton
      rhs=s_vm-3.0d0*shear*dpseq-syh
      do while((abs(rhs).gt.tol*syh).and.(itern<itern_max))
        itern=itern+1
        dpseq=dpseq+rhs/(3.0d0*shear+hh)
        syh=sy+hh*(pseq_t+dpseq)
        rhs=s_vm-3.0d0*shear*dpseq-syh
      enddo
      if(itern.eq.itern_max)then
        write(*,*)'ERROR no conv plastic'
        pnewdt=0.5
      endif

c     Update in N flow direction
      stress3(1:3)=nflow3(1:3)*syh+s_hyd
      eps_e(1:3)=eps_e(1:3)-3.0d0/2.0d0*nflow3(1:3)*dpseq
      stress3(4:6)=nflow3(4:6)*syh
      eps_e(4:6)=eps_e(4:6)-3.0d0*nflow3(4:6)*dpseq

      
c     Tangent  
      newshear=shear*syh/s_vm
      newbulk=(EE/(1.0d0-2.0d0*nu)-2.0d0*newshear)/3.0d0
      newhard=3.0d0*shear*hh/(3.0d0*shear+hh)-3.0d0*newshear
      do ii=1,3
        do jj=1,3
          ddsdde3(ii,jj)=newbulk
        enddo
        ddsdde3(ii,ii)=2.0d0*newshear+newbulk
      enddo
      do ii=4,6
        ddsdde3(ii,ii)=newshear
      enddo
      do ii=1,6
        do jj=1,6
          ddsdde3(ii,jj)=ddsdde3(ii,jj)+newhard*nflow3(ii)*nflow3(jj)
        enddo
      enddo
      c33=2.d0*newshear+newbulk+newhard*nflow3(3)*nflow3(3)
      endif

c     Check if stress out of plane is zero
      if(ndi.eq.2)then
      R2=abs(stress3(3))
      else
      R2=0.0d0
      endif
      enddo
      if(iterps.eq.iterps_max)then
        write(*,*)'ERROR no conv plane'
        pnewdt=0.5
      endif

c     Tangent in 2d 
      if(ndi.eq.2)then
      call M66INV(ddsdde3,ddsdde3inv)
      ddsddeinv(1,1)=ddsdde3inv(1,1)
      ddsddeinv(1,2)=ddsdde3inv(1,2)
      ddsddeinv(2,1)=ddsdde3inv(2,1)
      ddsddeinv(2,2)=ddsdde3inv(2,2)
      ddsddeinv(1,3)=ddsdde3inv(1,4)
      ddsddeinv(2,3)=ddsdde3inv(2,4)
      ddsddeinv(3,1)=ddsdde3inv(4,1)
      ddsddeinv(3,2)=ddsdde3inv(4,2)
      ddsddeinv(3,3)=ddsdde3inv(4,4)
      call M33INV(ddsddeinv,ddsdde)
      else
      ddsdde(1:6,1:6)=ddsdde3(1:6,1:6)
      endif

c     Stree in 2d 
      if(ndi.eq.2)then
      stress(1:2)=stress3(1:2)
      stress(3)=stress3(4)
      else
      stress(1:6)=stress3(1:6)
      endif

c     State
      statev(1:6)=eps_e(1:6)
      statev(7)=pseq_t+dpseq

      return
      end subroutine umat


      SUBROUTINE M33INV(A,AINV)
      implicit none
      real*8 A(3,3)
      real*8 AINV(3,3)
      real*8 DET
      real*8 COFACTOR(3,3)
      DET =   A(1,1)*A(2,2)*A(3,3)  
     1       - A(1,1)*A(2,3)*A(3,2)  
     1       - A(1,2)*A(2,1)*A(3,3)  
     1       + A(1,2)*A(2,3)*A(3,1)  
     1       + A(1,3)*A(2,1)*A(3,2)  
     1       - A(1,3)*A(2,2)*A(3,1)
      COFACTOR(1,1) = +(A(2,2)*A(3,3)-A(2,3)*A(3,2))
      COFACTOR(1,2) = -(A(2,1)*A(3,3)-A(2,3)*A(3,1))
      COFACTOR(1,3) = +(A(2,1)*A(3,2)-A(2,2)*A(3,1))
      COFACTOR(2,1) = -(A(1,2)*A(3,3)-A(1,3)*A(3,2))
      COFACTOR(2,2) = +(A(1,1)*A(3,3)-A(1,3)*A(3,1))
      COFACTOR(2,3) = -(A(1,1)*A(3,2)-A(1,2)*A(3,1))
      COFACTOR(3,1) = +(A(1,2)*A(2,3)-A(1,3)*A(2,2))
      COFACTOR(3,2) = -(A(1,1)*A(2,3)-A(1,3)*A(2,1))
      COFACTOR(3,3) = +(A(1,1)*A(2,2)-A(1,2)*A(2,1))
      AINV=TRANSPOSE(COFACTOR)/DET
      RETURN
      END SUBROUTINE M33INV


